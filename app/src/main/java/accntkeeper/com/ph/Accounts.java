package accntkeeper.com.ph;

public class Accounts {
    public String name, category, site_name, user_name, password;

    public Accounts(String name, String category, String site_name, String user_name, String password) {
        this.name = name;
        this.category = category;
        this.site_name = site_name;
        this.user_name = user_name;
        this.password = password;
    }
}
